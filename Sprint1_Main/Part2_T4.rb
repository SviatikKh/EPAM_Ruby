=begin
    
Змінна min має число від 0 до 59. Визначити в яку чверть години потрапляє число,
відповідно вивести на екран одне з значень: «перша», «друга», «третя»б
«четверта»
    
=end

min = gets.chomp()
x = min.to_i

if x >= 0 && x <= 15
    puts 'перша'   
elsif 
    x >= 16 && x <= 30
    puts 'друга'
elsif 
    x >= 31 && x <= 45
    puts 'третя'
else
    puts 'четверта'
   
end